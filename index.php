        <div class="text-container">
        <p class="text">Our “Salone” invites visitors to discover twelve projects conceived as ludic antidotes to a world filled with screens. Designed distractions that coerce us into playful and shared forms of interaction. Everyone plays. Electronic. Analog. Social. Play is a form of connection. Social platforms feign connecting us, but in the end they just leave us in limbo. Inversely, play is superficial by design, while secretly inviting us into a deeper understanding of each other and of our own limits.</p>
        <p class="text">Designed and developed by the Media Design Master of the –HEAD Genève within an identity designed by students of Visual Communication, the Salone Ludico invites visitors to explore the nature of play. Projects range in scope and scale from robotic mixologists and domotic hi-fi to infinite rooms where space itself becomes the play station. For the more adventurous, an underground club proposes a redefinition of the seedy games room with updated parlour games of strategy and chance.</p>
        </div>
